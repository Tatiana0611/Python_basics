# Напишите программу, которая принимает на вход координаты двух точек и находит расстояние между ними в 2D пространстве.
# Пример:
# - A (3,6); B (2,1) -> 5,09
# - A (7,-5); B (1,-1) -> 7,21

x1, y1 = [int(s) for s in input('Введите координаты точки А (x,y): ').split(',')]
x2, y2 = [int(s) for s in input('Введите координаты точки B (x,y): ').split(',')]
d = round((((x2 - x1) ** 2 + (y2 - y1) ** 2) ** 0.5), 2)
print(f'Расстояние между точками A ({x1},{y1}) и B ({x2},{y2}) равно {d}')
